// Absolute value
console.log("Absolute value: " + Math.abs(-10));

// Power
console.log("2 raised to the power 3: " + Math.pow(2, 3));

// Square root
console.log("Square root of 16: " + Math.sqrt(16));

// Maximum and Minimum
console.log("Max of 10 and 20: " + Math.max(10, 20));
console.log("Min of 10 and 20: " + Math.min(10, 20));

// Random number
console.log("Random number: " + Math.random());

// Rounding
console.log("Round 5.6: " + Math.round(5.6));
console.log("Ceil of 5.3: " + Math.ceil(5.3));
console.log("Floor of 5.7: " + Math.floor(5.7));

